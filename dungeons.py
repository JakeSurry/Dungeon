def blocks(room):
	block_list = []
	for y, row in enumerate(room):
		for x, block in enumerate(row):
			if block != 0 and block != 3:
				block_list.append([block, pg.Rect(x*40, y*40, 40, 40)])
	return block_list

def display_dungeon(display, room):
	for y, row in enumerate(room):
		for x, block in enumerate(row):
			try:
				display.blit(images[block], (x*40, y*40, 40, 40))
			except TypeError:
				display.blit(images[block[0]], (x*40, y*40, 40, 40))

